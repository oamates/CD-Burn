#define	DEFAULT_ERROR_UNKNOWN	("ERROR(UNKNOWN)")

const char* GetErrorCodeString(int nErrorCode)
{
	int	nIndex	= 0;

	for(nIndex = 0; nIndex <  (int)COUNT_OF(gErrorCodeTable); nIndex ++)
	{
		if(gErrorCodeTable[nIndex].m_nErrorCode == (DWORD)nErrorCode)
		{
			break;
		}
	}

	if(nIndex >= 0 && nIndex < (int)COUNT_OF(gErrorCodeTable))
	{
		return gErrorCodeTable[nIndex].m_sErrorCodeString;
	}
	return DEFAULT_ERROR_UNKNOWN;
}
